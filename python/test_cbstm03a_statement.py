"""
Regression tests for the CBSTM03A statement generation migration.

These tests verify that the Python migration of the COBOL CREASTMT batch
program (CBSTM03A.CBL + CBSTM03B.CBL) produces byte-identical output to
the committed reference files.

Test fixtures
-------------
Input data (CSV, under ``fixtures/``):
    trnx.csv   -- 3 transactions across 2 cards
    xref.csv   -- 2 card cross-reference records
    cust.csv   -- 2 customer records
    acct.csv   -- 2 account records

Reference output (under ``reference/``):
    expected_statement.txt   -- plain-text statement (80-byte records)
    expected_statement.html  -- HTML statement (100-byte records)

The reference files were generated by running::

    python cbstm03a_statement.py \\
        fixtures/trnx.csv fixtures/xref.csv \\
        fixtures/cust.csv fixtures/acct.csv \\
        reference/expected_statement.txt reference/expected_statement.html

after the initial trusted validation against the COBOL output specification.
To regenerate after a legitimate output change, re-run the command above and
commit the updated reference files.

Test data design
-----------------
* Card 4111111111111111 (customer John Michael Smith, account 80000000001):
  two transactions -- one positive ($45.67) and one negative (-$22.50) to
  exercise trailing-minus sign formatting and correct total computation.
* Card 4222222222222222 (customer Jane Ann Doe, account 80000000002):
  one transaction ($128.99) to verify multi-statement output.
"""

