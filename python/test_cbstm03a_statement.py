class TestFileStore:
    """Basic I/O layer tests."""

    def test_sequential_read_trnx(self):
        store = FileStore(
            str(_FIXTURES / "trnx.csv"),
            str(_FIXTURES / "xref.csv"),
            str(_FIXTURES / "cust.csv"),
            str(_FIXTURES / "acct.csv"),
        )
        rc = store.open("TRNXFILE")
        assert rc == "00"

        rc, rec = store.read_sequential("TRNXFILE")
        assert rc == "00"
        assert rec.trnx_card_num == "4111111111111111"
        assert rec.trnx_amt == Decimal("45.67")

    def test_keyed_read_customer(self):
        store = FileStore(
            str(_FIXTURES / "trnx.csv"),
            str(_FIXTURES / "xref.csv"),
            str(_FIXTURES / "cust.csv"),
            str(_FIXTURES / "acct.csv"),
        )
        store.open("CUSTFILE")
        rc, rec = store.read_by_key("CUSTFILE", "100000001", 9)
        assert rc == "00"
        assert rec.cust_first_name == "John"

    def test_keyed_read_not_found(self):
        store = FileStore(
            str(_FIXTURES / "trnx.csv"),
            str(_FIXTURES / "xref.csv"),
            str(_FIXTURES / "cust.csv"),
            str(_FIXTURES / "acct.csv"),
        )
        store.open("CUSTFILE")
        rc, rec = store.read_by_key("CUSTFILE", "999999999", 9)
        assert rc == "23"
        assert rec is None

    def test_sequential_read_eof(self):
        store = FileStore(
            str(_FIXTURES / "trnx.csv"),
            str(_FIXTURES / "xref.csv"),
            str(_FIXTURES / "cust.csv"),
            str(_FIXTURES / "acct.csv"),
        )
        store.open("XREFFILE")
        store.read_sequential("XREFFILE")
        store.read_sequential("XREFFILE")
        rc, rec = store.read_sequential("XREFFILE")
        assert rc == "10"
        assert rec is None


