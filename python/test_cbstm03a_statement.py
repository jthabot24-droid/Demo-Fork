class TestPic9_99Minus:
    """PIC 9(9).99- -- no zero suppression, trailing sign."""

    def test_positive_value(self):
        assert format_pic_9_99_minus(Decimal("1500.75")) == "000001500.75 "

    def test_negative_value(self):
        assert format_pic_9_99_minus(Decimal("-1500.75")) == "000001500.75-"

    def test_zero(self):
        assert format_pic_9_99_minus(Decimal("0.00")) == "000000000.00 "

    def test_large_value(self):
        assert format_pic_9_99_minus(Decimal("999999999.99")) == "999999999.99 "

    def test_width(self):
        result = format_pic_9_99_minus(Decimal("42.00"))
        assert len(result) == 13


class TestPicZ_99Minus:
    """PIC Z(9).99- -- zero-suppressed integer, trailing sign."""

    def test_positive_value(self):
        assert format_pic_z_99_minus(Decimal("45.67")) == "       45.67 "

    def test_negative_value(self):
        assert format_pic_z_99_minus(Decimal("-22.50")) == "       22.50-"

