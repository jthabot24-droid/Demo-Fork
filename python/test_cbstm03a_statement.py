class TestStatementRegression:
    """Compare generated output against committed reference files."""

    @pytest.fixture(autouse=True)
    def setup_output_files(self, tmp_path):
        self.stmt_output = str(tmp_path / "statement.txt")
        self.html_output = str(tmp_path / "statement.html")
        generate_statements(
            str(_FIXTURES / "trnx.csv"),
            str(_FIXTURES / "xref.csv"),
            str(_FIXTURES / "cust.csv"),
            str(_FIXTURES / "acct.csv"),
            self.stmt_output,
            self.html_output,
        )

    def test_plain_text_byte_identical(self):
        """Plain-text output must match reference/expected_statement.txt."""
        expected = (_REFERENCE / "expected_statement.txt").read_text()
        actual = Path(self.stmt_output).read_text()
        assert actual == expected, (
            "Plain-text statement output differs from reference. "
            "Run the generator and diff against reference/expected_statement.txt."
        )

    def test_html_byte_identical(self):
        """HTML output must match reference/expected_statement.html."""
        expected = (_REFERENCE / "expected_statement.html").read_text()
        actual = Path(self.html_output).read_text()
        assert actual == expected, (
            "HTML statement output differs from reference. "
            "Run the generator and diff against reference/expected_statement.html."
        )

    def test_plain_text_record_width(self):
        """Every record in the plain-text output must be exactly 80 bytes."""
        with open(self.stmt_output) as f:
            for i, line in enumerate(f, 1):
                content = line.rstrip("\n")
                assert len(content) == 80, (
                    f"Line {i} is {len(content)} chars (expected 80): {content!r}"
                )

    def test_html_record_width(self):
        """Every record in the HTML output must be exactly 100 bytes."""
        with open(self.html_output) as f:
            for i, line in enumerate(f, 1):
                content = line.rstrip("\n")
                assert len(content) == 100, (
                    f"Line {i} is {len(content)} chars (expected 100): {content!r}"
                )

    def test_plain_text_line_count(self):
        """Verify the expected number of output lines."""
        with open(self.stmt_output) as f:
            lines = f.readlines()
        assert len(lines) == 41

    def test_html_line_count(self):
        """Verify the expected number of HTML output lines."""
        with open(self.html_output) as f:
            lines = f.readlines()
        assert len(lines) == 161

    def test_negative_amount_trailing_minus(self):
        """Gas Station transaction (-$22.50) must show trailing minus."""
        with open(self.stmt_output) as f:
            lines = f.readlines()
        gas_line = lines[17].rstrip("\n")
        assert gas_line.endswith("22.50-"), (
            f"Expected trailing minus for negative amount: {gas_line!r}"
        )

    def test_total_accounts_for_negative(self):
        """Total for first statement: 45.67 + (-22.50) = 23.17."""
        with open(self.stmt_output) as f:
            lines = f.readlines()
        total_line = lines[19].rstrip("\n")
        assert "23.17" in total_line, (
            f"Expected total 23.17 for first statement: {total_line!r}"
        )
